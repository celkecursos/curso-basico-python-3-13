nome_cliente = "Cesar"      # String
print("Nome:", nome_cliente)

idade_cliente = 18          # Inteiro
print("Idade:", idade_cliente)

altura_cliente = 1.81       # Float
print("Altura:", altura_cliente)

situacao = True             # Booleano
print("Situação:", situacao)
situacao = False
print("Situação:", situacao)

numeros = [2, 3, 5, 7, 11]  # Lista
print("Números:", numeros)
print("Numero:", numeros[2])

usuario = {                 # Dicionário
    "nome": "Cesar",
    "idade": 18,
    "cidade": "Curitiba"
}
print("Nome:", usuario["nome"])
print("Idade:", usuario["idade"])
print("Cidade:", usuario["cidade"])

curso = ("Python para iniciante", "Cesar", 2024, 197.52) # Tupla
